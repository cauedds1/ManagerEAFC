export function getLocationLabel(lang: Lang, loc: "casa" | "fora" | "neutro"): string {
  const t = PARTIDAS[lang];
  if (loc === "casa") return t.locationHome;
  if (loc === "fora") return t.locationAway;
  return t.locationNeutral;
}

export function getGoalTypeLabel(lang: Lang, type: string): string {
  const t = PARTIDAS[lang];
  const map: Record<string, string> = {
    normal: t.goalNormal,
    cabeca: t.goalHead,
    bicicleta: t.goalBike,
    voleio: t.goalVolley,
    fora_area: t.goalLong,
    falta: t.goalFree,
    penalti: t.goalPenalty,
    contra_ataque: t.goalCounter,
  };
  return map[type] ?? type;
}

export function getResultPill(lang: Lang, result: "vitoria" | "empate" | "derrota"): string {
  const t = PARTIDAS[lang];
  if (result === "vitoria") return t.resultV;
  if (result === "empate") return t.resultE;
  return t.resultD;
}

export function getRatingLabel(lang: Lang, rating: number): string {
  const t = PARTIDAS[lang];
  if (rating < 5.0) return t.ratingPoor;
  if (rating < 6.0) return t.ratingBelow;
  if (rating < 7.0) return t.ratingAverage;
  if (rating < 8.0) return t.ratingGood;
  if (rating < 9.0) return t.ratingGreat;
  return t.ratingExcellent;
}

export const getResultLabel = getResultPill;

export function matchDateLocale(lang: Lang): string {
  return lang === "en" ? "en-US" : "pt-BR";
}

